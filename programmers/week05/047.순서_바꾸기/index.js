/* -------------------------------------------------------------------------- */

const solution = (nums, n) => {
  return [...nums.slice(n), ...nums.slice(0, n)];
};

console.log(solution([2, 1, 6], 1)); // [1, 6, 2]
console.log(solution([5, 2, 1, 7, 5], 3)); // [7, 5, 5, 2, 1]
