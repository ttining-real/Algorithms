/* -------------------------------------------------------------------------- */
/*                           n보다 커질 때까지 더하기                           */
/* -------------------------------------------------------------------------- */

const solution = (nums, n) => {
  let sum = 0;

  for (const num of nums) {
    sum += num;
    if (sum > n) return sum;
  }
};

console.log(solution([34, 5, 71, 29, 100, 34], 123)); // 139
console.log(solution([58, 44, 27, 10, 100], 139)); // 239
