/* -------------------------------------------------------------------------- */
/*                              n개 간격의 원소들                              */
/* -------------------------------------------------------------------------- */

const solution = (nums, n) => nums.filter((_, idx) => idx % n === 0);

console.log(solution([4, 2, 6, 1, 7, 6], 2)); // [4, 6, 7]
console.log(solution([4, 2, 6, 1, 7, 6], 4)); // [4, 7]
