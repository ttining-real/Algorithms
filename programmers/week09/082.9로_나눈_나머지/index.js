/* -------------------------------------------------------------------------- */
/*                               9로 나눈 나머지                               */
/* -------------------------------------------------------------------------- */

const solution = (num) => {
  const sum = [...num].reduce((acc, cur) => acc + Number(cur), 0);
  return sum % 9;
};

console.log(solution("123")); // 6
console.log(solution("78720646226947352489")); // 2
