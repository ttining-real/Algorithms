const solution = (ineq, eq, n, m) => {
  const conditions = {
    "<=": n <= m,
    ">=": n >= m,
    "<!": n < m,
    ">!": n > m,
  };

  return conditions[ineq + eq] ? 1 : 0;
};

console.log(solution("<", "=", 20, 50)); // 1
console.log(solution(">", "!", 41, 78)); // 0
