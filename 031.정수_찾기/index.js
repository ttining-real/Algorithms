/*                                  정수 찾기                                  */
/* -------------------------------------------------------------------------- */

const solution = (nums, n) => (nums.includes(n) ? 1 : 0);

console.log(solution([1, 2, 3, 4, 5], 3)); // 1
console.log(solution([15, 98, 23, 2, 15], 20)); // 0
