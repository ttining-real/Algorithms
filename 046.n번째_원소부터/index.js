/* -------------------------------------------------------------------------- */
/*                               n번째 원소부터                                */
/* -------------------------------------------------------------------------- */

const solution = (nums, n) => {
  return nums.slice(n - 1);
};

console.log(solution([2, 1, 6], 3)); // [6]
console.log(solution([5, 2, 1, 7, 5], 2)); // [2, 1, 7, 5]
